int gcd(long long a, long long b)
{
    if (b == 0)
        return a;
    return gcd(b, a % b);
}

int sumOfDigits(long long a)
{
    int n = 0;

    while (a > 0)
    {
        n += a % 10;
        a /= 10;
    }

    return n;
}

