void solve(int cc)
{
    string s;
    cin >> s;

    bool allCaps = true;

    for (int i = 1; i < s.size(); i++)
    {
        if (s[i] > 'Z')
        {
            allCaps = false;
            break;
        }
    }

    if (allCaps)
    {
        if (s[0] > 'Z')
        {
            s[0] = (char)(s[0] - 32);
        }
        else
        {
            s[0] = (char)(s[0] + 32);
        }

        for (int i = 1; i < s.size(); i++)
        {
            s[i] = (char)(s[i] + 32);
        }
    }

    cout << s << '\n';
}

