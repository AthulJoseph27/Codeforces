void solve(int cc)
{
    long long a, b, x, y;
    cin >> x >> y;
    cin >> a >> b;
    long long cost = 0;
    if ((2 * a < b) || (x * y < 0))
    {
        cost = (abs(x) + abs(y)) * a;
    }
    else
    {
        if (x < 0)
        {
            long long m = max(x, y);
            cost = b * (abs(m));
            x -= m;
            y -= m;
            cost += a * (abs(x) + abs(y));
        }
        else
        {
            long long m = min(x, y);
            cost = b * (abs(m));
            x -= m;
            y -= m;
            cost += a * (abs(x) + abs(y));
        }
    }
    cout << cost << '\n';
}

