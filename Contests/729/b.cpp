void solve(int cc)
{
    long long n, a, b;
    cin >> n >> a >> b;

    // (a+xb)*a^y => a^y+1 + xb*a^y
    // xb+1 , ((b+1)a^x)+yb) => a^x * b + a^x + yb => a^x * b * (y+1) + a^x

    if (a == 1)
    {
        if (b == 1 || n % b == 1)
        {
            cout << "Yes" << '\n';
        }
        else
        {
            cout << "No" << '\n';
        }
        return;
    }
    else
    {
        long long sub = 1;
        while (sub <= n)
        {
            if (b == 1 || (n - sub) % b == 0)
            {
                cout << "Yes" << '\n';
                return;
            }
            sub *= a;
        }

        cout << "No" << '\n';
    }
}

