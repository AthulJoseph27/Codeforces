bool isGood(vector<int> &a)
{
    int n = a.size();
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            for (int k = 0; k < n; k++)
            {
                if ((i == j) || (j == k) || (i == k))
                    continue;
                int ik = abs(i - k) + abs(a[i] - a[k]);
                int ij = abs(i - j) + abs(a[i] - a[j]);
                int jk = abs(j - k) + abs(a[j] - a[k]);
                if (ik == (ij + jk))
                    return false;
            }
        }
    }
    return true;
}

void solve(int cc)
{
    // subset of size > 4 is impossible
    int n;
    cin >> n;

    vector<int> a(n);

    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    int count = 0;

    for (int l = 0; l < n; l++)
    {
        for (int r = l + 1; r <= n; r++)
        {
            if ((r - l) > 4)
                break;
            if ((r - l) < 3)
            {
                count++;
                continue;
            }
            vector<int> temp;

            for (int k = l; k < r; k++)
                temp.push_back(a[k]);

            if (isGood(temp))
                count++;
        }
    }

    cout << count << endl;
}

