void solve(int cc)
{
    long long n, H;
    cin >> n >> H;

    vector<long long> a(n);

    for (long long &i : a)
        cin >> i;

    sort(all(a));

    long long dm = a[n - 1] + a[n - 2];

    // debug(a);
    // debug(H, dm);

    long long ans = H / dm * 2;

    H = H % dm;

    // debug(H, ans);
    if (H > 0 && a[n - 1] >= H)
    {
        ans += 1;
        cout << ans << '\n';
        return;
    }

    ans += H / a[n - 1];

    H = H % a[n - 1];

    if (H > 0 && H <= a[n - 2])
    {
        cout << ans + 1 << '\n';
        return;
    }

    ans += H / a[n - 2];

    cout << ans << '\n';
}

