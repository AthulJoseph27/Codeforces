ll minimize(vl &a, ll k)
{
    ll dist = 0;

    for (int i = a.size() - 1; i >= 0; i -= k)
    {
        dist += a[i];
    }

    return dist;
}

void solve(int cc)
{
    ll n, k;

    cin >> n >> k;

    vl a(n);

    for (ll &i : a)
        cin >> i;

    sort(all(a));

    ll mx = max(llabs(a[0]), llabs(a.back()));

    vvl b(2);

    for (ll i : a)
    {
        if (i < 0)
            b[0].push_back(llabs(i));
        else
            b[1].push_back(i);
    }

    reverse(all(b[0]));

    ll ans = 2ll * (minimize(b[0], k) + minimize(b[1], k)) - mx;

    cout << ans << '\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T;

    cin >> T;

    for (int i = 0; i < T; i++)
    {
        solve(i + 1);
    }

    return 0;
}