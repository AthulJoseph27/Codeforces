void solve(int cc)
{
    /*
        1101010101011001010
        1010011010010101010

        1000010000010001010

        at each time, we sub, the common bits gets set to zero

        0 1 2 3 4 5 6 ...... 30
        a b c d .....         ?


        numbers which are divide all count without reminder will work

    */

    int n;
    cin >> n;

    vi a(n);

    for (int &i : a)
        cin >> i;

    vi cnt(30, 0);

    for (int i : a)
    {
        for (int j = 0; j < 30; j++)
        {
            cnt[j] += ((i >> j) & 1); //checking if jth bit is set
        }
    }

    for (int k = 1; k <= n; k++)
    {
        bool div = true;
        for (int j : cnt)
        {
            if (j % k != 0)
            {
                div = false;
                break;
            }
        }

        if (div)
            cout << k << ' ';
    }

    cout << '\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T;

    cin >> T;

    for (int i = 0; i < T; i++)
    {
        solve(i + 1);
    }

    return 0;
}