void solve(int cc)
{
    ll n;
    cin >> n;

    vl a(n);

    for (ll &i : a)
        cin >> i;

    ll sm = 0;

    for (ll i : a)
        sm += i;

    // sum shd be divisible by (n-1) and no element should be greater than sm/(n-1)

    ll ans = 0;
    if (sm % (n - 1) != 0)
    {
        ll x = sm / (n - 1);
        ans = ((n - 1ll) * (x + 1ll) - sm);
    }

    ll mx = *max_element(all(a));
    ll element = (sm + ans) / (n - 1ll);

    if (mx > element)
    {
        ans = (mx * (n - 1ll)) - sm;
    }

    cout << ans << '\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T;

    cin >> T;

    for (int i = 0; i < T; i++)
    {
        solve(i + 1);
    }

    return 0;
}

