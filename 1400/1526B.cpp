int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    vector<long long> coins;

    string s = "11";

    while (s.size() <= 10)
    {
        coins.push_back(stoll(s));
        s += '1';
    }

    dp[0] = true;

    for (long long c : coins)
    {
        for (int i = 0; (i + c) < dp.size(); i++)
        {
            if (dp[i])
            {
                dp[i + c] = true;
            }
        }
    }

    int T;

    cin >> T;

    for (int i = 0; i < T; i++)
    {
        solve(i + 1);
    }

    return 0;
}