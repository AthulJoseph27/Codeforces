void solve(int cc)
{
    int n;
    cin >> n;

    vi a(n);

    for (int &i : a)
        cin >> i;

    int sm = 0;

    for (int i : a)
        sm += i;

    int ans = n - 1;

    for (int len = n; len >= 2; len--)
    {
        if (sm % len != 0)
            continue;

        int ele = sm / len;

        int cur = 0;
        bool flag = true;
        for (int i = 0; i < n; i++)
        {
            cur += a[i];
            if (cur > ele)
            {
                flag = false;
                break;
            }
            if (cur == ele)
            {
                cur = 0;
            }
        }

        if (flag)
        {
            cout << (n - len) << '\n';
            return;
        }
    }
    cout << ans << '\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T;

    cin >> T;

    for (int i = 0; i < T; i++)
    {
        solve(i + 1);
    }

    return 0;
}