void solve(int cc)
{
    ll A, B, n;
    cin >> A >> B >> n;

    vector<pair<ll, ll>> a(n);

    for (int i = 0; i < n; i++)
    {
        cin >> a[i].first;
    }

    for (int i = 0; i < n; i++)
    {
        cin >> a[i].second;
    }

    sort(all(a), [&](const pair<int, int> &aa, const pair<int, int> &bb)
         { return aa.first < bb.first; });

    int killed = 0;

    for (int i = 0; i < n; i++)
    {
        if (B <= 0)
            break;

        ll strikes = a[i].second / A;

        if (a[i].second % A != 0)
            strikes++;

        ll damage = strikes * a[i].first;

        ll strike_hero = B / a[i].first;

        B -= damage;

        if (B % a[i].first != 0)
            strike_hero++;

        if (strike_hero >= strikes)
            killed++;
        else
            break;
    }

    if (killed == n)
        cout << "YES\n";
    else
        cout << "NO\n";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T;

    cin >> T;

    for (int i = 0; i < T; i++)
    {
        solve(i + 1);
    }

    return 0;
}