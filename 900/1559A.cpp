void solve(int cc)
{
    int n;
    cin >> n;

    vi a(n);

    for (int &i : a)
        cin >> i;

    bool has_zero = true;

    vector<bool> zero(32, false);

    for (int i = 0; i < n; i++)
    {
        string s = std::bitset<32>(a[i]).to_string();

        for (int j = 0; j < 32; j++)
        {
            if (s[j] == '0')
                zero[j] = true;
        }
    }

    for (int i = 0; i < 32; i++)
    {
        has_zero = (has_zero & zero[i]);
    }

    if (has_zero)
    {
        cout << 0 << '\n';
        return;
    }

    int ans = 0;

    for (int i = 31; i >= 0; i--)
    {
        if (!zero[i])
            ans += (1 << (31 - i));
    }

    cout << ans << '\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T;

    cin >> T;

    for (int i = 0; i < T; i++)
    {
        solve(i + 1);
    }

    return 0;
}