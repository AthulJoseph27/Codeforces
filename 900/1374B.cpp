bool impossible = false;

int getCount(int n)
{
    if (n == 1)
        return 0;

    if (n % 6 == 0)
        return 1 + getCount(n / 6);

    if (n % 3 != 0)
    {
        impossible = true;
        return -1;
    }
    return 2 + getCount(n / 3);
}

