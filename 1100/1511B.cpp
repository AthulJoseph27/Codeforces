void solve(int cc)
{
    int a, b, c;
    cin >> a >> b >> c;

    // get a prime number with c digits

    vector<int> prime = {2, 11, 103, 1009, 10009, 100003, 1000003, 10000019, 100000007};

    int ff1 = 1, ff2 = 1;

    while (true)
    {
        int l1 = to_string(ff1 * prime[c - 1]).size();

        if (l1 == a)
            break;
        ff1 *= 2;

        if (l1 > a)
            assert(false);
    }

    while (true)
    {
        int l1 = to_string(ff2 * prime[c - 1]).size();
        if (l1 == b)
            break;
        ff2 *= 3;
        if (l1 > b)
            assert(false);
    }

    long long a1 = prime[c - 1] * ff1;
    long long a2 = prime[c - 1] * ff2;

    cout << a1 << ' ' << a2 << '\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int T;

    cin >> T;

    for (int i = 0; i < T; i++)
    {
        solve(i + 1);
    }

    return 0;
}