void solve(int cc)
{
    long long x;
    cin >> x;

    /*
        h-> cells
        1-> 1
        2-> no nice formation
        3-> 6 cells 3*2
        4-> no nice formation
        5-> no nice formation
        6-> no nice formation
        7-> 28 -> 7*4 -> n*(n+1)/2

        1,3,7 -> 2^k -1
    */

    int ans = 0;

    for (int i = 1; i <= 32; i++)
    {
        long long p2 = (1LL << i) - 1;
        long long cnt = p2 * (p2 + 1) / 2LL;
        if (x >= cnt)
        {
            ans++;
            x -= cnt;
        }
    }
    cout << ans - 1 << '\n';
}

